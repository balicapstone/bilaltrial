package DataClasses;

public class Image {
	public String image;
	public String caption;
	public String screenImageID;
	
	public Image(){
		
	}
	
	public void setImage(String i){
		image = i;
	}
	
	public void setCaption(String c){
		caption = c;
	}
	
	public void setScreenImageID(String id){
		screenImageID = id;
	}
}
